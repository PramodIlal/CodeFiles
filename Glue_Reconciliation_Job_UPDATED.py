def write_reconciled_target_glue(spark, reconciled_df, output_path, file_format, overwrite_original=False):
    """
    Glue-safe wrapper around existing write logic.
    Writes to local temp if output is S3, then uploads to S3.
    """
    if not is_s3_path(output_path):
        return write_reconciled_target(spark, reconciled_df, output_path, file_format, overwrite_original)

    # S3 target -> write to /tmp first
    local_tmp_file = f"/tmp/{os.path.basename(output_path)}"
    print(f"[INFO] S3 output detected. Writing locally first: {local_tmp_file}")

    # Reuse existing writer
    write_reconciled_target(spark, reconciled_df, local_tmp_file, file_format, overwrite_original=False)

    # Upload to S3
    s3 = boto3.client("s3")
    bucket, key = parse_s3_path(output_path)
    s3.upload_file(local_tmp_file, bucket, key)

    print(f"[OK] Uploaded reconciled target to S3: {output_path}")



def write_dynamodb_table(glueContext, df, table_name):
    """
    Writes Spark DataFrame to DynamoDB table.
    NOTE:
      - DynamoDB write behaves like upsert based on table keys
      - It does NOT delete old extra items automatically
    """
    print(f"\n[INFO] Writing reconciled output to DynamoDB table: {table_name}")

    dyf = DynamicFrame.fromDF(df, glueContext, "reconciled_dyf")

    glueContext.write_dynamic_frame.from_options(
        frame=dyf,
        connection_type="dynamodb",
        connection_options={
            "dynamodb.output.tableName": table_name,
            "dynamodb.throughput.write.percent": "0.5"
        }
    )

    print(f"[OK] Reconciled output written to DynamoDB: {table_name}")


