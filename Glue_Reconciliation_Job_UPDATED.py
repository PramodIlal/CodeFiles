def perform_deletes(target_df, extra_in_target_df, primary_key_columns):
    """
    Deletes records from target that don't exist in source.
    Uses left_anti join.
    """
    print_section("PERFORMING DELETE OPERATIONS")

    if len(extra_in_target_df) == 0:
        print("[INFO] No records to delete.")
        return target_df, {"total_deletes": 0, "delete_details": []}

    delete_stats = {
        "total_deletes": 0,
        "delete_details": []
    }

    print(f"[INFO] Records to delete: {len(extra_in_target_df)}")

    initial_count = target_df.count()
    print(f"[INFO] Target count before delete: {initial_count}")

    # Convert pandas to Spark
    spark = target_df.sparkSession
    print(f"[INFO] Converting pandas DataFrame to Spark DataFrame...")
    extra_in_target_spark_df = spark.createDataFrame(extra_in_target_df)

    # Left anti join
    print(f"[INFO] Filtering out records to delete...")
    updated_target_df = target_df.join(
        extra_in_target_spark_df,
        on=primary_key_columns,
        how="left_anti"
    )

    print(f"[INFO] Counting remaining records...")
    final_count = updated_target_df.count()
    deleted_count = initial_count - final_count

    print(f"[INFO] Target count after delete: {final_count}")
    print(f"[INFO] Records deleted: {deleted_count}")

    # Build delete details
    detail_limit = min(1000, len(extra_in_target_df))
    pk_values_list = []
    for idx, row in extra_in_target_df.head(detail_limit).iterrows():
        pk_values = {pk_col: row[pk_col] for pk_col in primary_key_columns}
        pk_values_list.append(pk_values)

    delete_stats["total_deletes"] = deleted_count
    delete_stats["delete_details"] = pk_values_list
    if len(extra_in_target_df) > detail_limit:
        delete_stats["delete_details_note"] = f"Limited to first {detail_limit} records"

    print(f"[OK] Successfully deleted {deleted_count} records.")
    print(f"[SUMMARY] Total records deleted: {delete_stats['total_deletes']}")

    return updated_target_df, delete_stats

