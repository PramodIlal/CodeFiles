def read_json_from_s3(s3_path):
    """
    Reads JSON file from S3 and returns dict
    """
    s3 = boto3.client("s3")
    bucket, key = parse_s3_path(s3_path)
    obj = s3.get_object(Bucket=bucket, Key=key)
    content = obj["Body"].read().decode("utf-8")
    return json.loads(content)


