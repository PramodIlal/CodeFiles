def perform_inserts(target_df, source_df, extra_in_source_df, primary_key_columns):
    """
    Inserts records from source that are missing in target.
    FIXED: Ensures column order matches target before union.
    """
    print_section("PERFORMING INSERT OPERATIONS")

    if len(extra_in_source_df) == 0:
        print("[INFO] No records to insert.")
        return target_df, {"total_inserts": 0, "insert_details": []}

    insert_stats = {
        "total_inserts": 0,
        "insert_details": []
    }

    print(f"[INFO] Records to insert: {len(extra_in_source_df)}")

    # Convert pandas to Spark
    spark = source_df.sparkSession
    print(f"[INFO] Converting pandas DataFrame to Spark DataFrame...")
    extra_in_source_spark_df = spark.createDataFrame(extra_in_source_df)

    # Join to fetch records
    print(f"[INFO] Joining source with insert keys...")
    records_to_insert = source_df.join(
        extra_in_source_spark_df,
        on=primary_key_columns,
        how="inner"
    )

    # Count
    print(f"[INFO] Counting matched records...")
    insert_count = records_to_insert.count()

    if insert_count == 0:
        print("[WARNING] No matching records found in source.")
        return target_df, insert_stats

    print(f"[INFO] Found {insert_count} records to insert")

    # CRITICAL FIX: Align column order with target
    target_column_order = target_df.columns
    print(f"[DEBUG] Target column order: {target_column_order}")
    print(f"[DEBUG] Source columns before reorder: {records_to_insert.columns}")

    # Select columns in target's order
    records_to_insert_aligned = records_to_insert.select(*target_column_order)

    print(f"[DEBUG] Source columns after reorder: {records_to_insert_aligned.columns}")

    # Union - now both have same column order
    print(f"[INFO] Appending {insert_count} records to target...")
    updated_target_df = target_df.union(records_to_insert_aligned)

    # Build insert details
    detail_limit = min(1000, len(extra_in_source_df))
    pk_values_list = []
    for idx, row in extra_in_source_df.head(detail_limit).iterrows():
        pk_values = {pk_col: row[pk_col] for pk_col in primary_key_columns}
        pk_values_list.append(pk_values)

    insert_stats["total_inserts"] = insert_count
    insert_stats["insert_details"] = pk_values_list
    if len(extra_in_source_df) > detail_limit:
        insert_stats["insert_details_note"] = f"Limited to first {detail_limit} records"

    print(f"[OK] Successfully inserted {insert_count} records.")
    print(f"[SUMMARY] Total records inserted: {insert_stats['total_inserts']}")

    return updated_target_df, insert_stats

