def create_backup(file_path):
    """Creates a timestamped backup of the original file (file targets only)."""
    import shutil

    if not os.path.exists(file_path):
        print(f"[WARNING] Original file not found: {file_path}")
        return None

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    file_dir = os.path.dirname(file_path)
    file_name = os.path.basename(file_path)
    file_name_no_ext, file_ext = os.path.splitext(file_name)

    backup_name = f"{file_name_no_ext}_backup_{timestamp}{file_ext}"
    backup_path = os.path.join(file_dir, backup_name)

    try:
        shutil.copy2(file_path, backup_path)
        print(f"[OK] Backup created: {backup_path}")
        return backup_path
    except Exception as e:
        print(f"[ERROR] Backup failed: {str(e)}")
        return None

