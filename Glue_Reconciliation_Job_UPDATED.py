    print_section("PERFORMING UPDATE OPERATIONS")

    if len(detailed_mismatch_df) == 0:
        print("[INFO] No records to update.")
        return target_df, {"total_updates": 0, "updated_records": 0, "updated_columns": {}}

    update_stats = {
        "total_updates": 0,
        "updated_records": 0,
        "updated_columns": {},
        "update_details": []
    }

    print(f"[INFO] Records to update: {len(detailed_mismatch_df)}")

    # Debug: Show what columns exist
    print(f"[DEBUG] Target columns: {sorted(target_df.columns)}")
    print(f"[DEBUG] Source columns: {sorted(source_df.columns)}")

    # Collect all unique columns that need updating
    all_mismatched_cols = set()
    column_update_count = {}

    for idx, row in detailed_mismatch_df.iterrows():
        mismatched_cols = parse_mismatched_columns(row.get("mismatched_columns", ""))
        all_mismatched_cols.update(mismatched_cols)
        for col in mismatched_cols:
            column_update_count[col] = column_update_count.get(col, 0) + 1

    print(f"[INFO] Unique columns to update: {all_mismatched_cols}")
    print(f"[INFO] Column update counts: {column_update_count}")

    # Verify all columns exist in both DataFrames
    target_cols_set = set(target_df.columns)
    source_cols_set = set(source_df.columns)

    missing_in_target = [col for col in all_mismatched_cols if col not in target_cols_set]
    missing_in_source = [col for col in all_mismatched_cols if col not in source_cols_set]

    if missing_in_target:
        print(f"[ERROR] Columns missing in target: {missing_in_target}")
        print("[ERROR] This should not happen if schema enforcement is working correctly!")
        raise ValueError(f"Target missing columns: {missing_in_target}")

    if missing_in_source:
        print(f"[ERROR] Columns missing in source: {missing_in_source}")
        raise ValueError(f"Source missing columns: {missing_in_source}")

    # Convert pandas to Spark for join
    spark = target_df.sparkSession
    mismatch_keys_df = spark.createDataFrame(detailed_mismatch_df[primary_key_columns])

    print(f"[INFO] Converted {len(detailed_mismatch_df)} mismatch keys to Spark DataFrame")

    # Select only needed columns from source
    source_cols_needed = list(primary_key_columns) + list(all_mismatched_cols)
    source_subset = source_df.select(*source_cols_needed)

    # Join source with mismatch keys
    print(f"[INFO] Joining source with mismatch keys...")
    source_correct_values = source_subset.join(
        mismatch_keys_df,
        on=primary_key_columns,
        how="inner"
    )

    # Add suffix to source columns
    for col in source_correct_values.columns:
        if col not in primary_key_columns:
            source_correct_values = source_correct_values.withColumnRenamed(col, f"{col}_src_correct")

    print(f"[INFO] Joining target with correct values...")
    target_with_correct = target_df.join(
        source_correct_values,
        on=primary_key_columns,
        how="left"
    )

    # Update columns
    print(f"[INFO] Updating columns...")
    for col_name in all_mismatched_cols:
        src_col_name = f"{col_name}_src_correct"
        if src_col_name in target_with_correct.columns:
            target_with_correct = target_with_correct.withColumn(
                col_name,
                F.coalesce(F.col(src_col_name), F.col(col_name))
            )
            update_stats["updated_columns"][col_name] = column_update_count.get(col_name, 0)
            print(f"[INFO] Updated column: {col_name}")

    # Drop temporary columns
    updated_target_df = target_with_correct.select(target_df.columns)

    update_stats["updated_records"] = len(detailed_mismatch_df)
    update_stats["total_updates"] = sum(column_update_count.values())

    # Build update details
    detail_limit = min(1000, len(detailed_mismatch_df))
    for idx, row in detailed_mismatch_df.head(detail_limit).iterrows():
        pk_values = {pk_col: row[pk_col] for pk_col in primary_key_columns}
        mismatched_cols = parse_mismatched_columns(row.get("mismatched_columns", ""))
        update_stats["update_details"].append({
            "primary_keys": pk_values,
            "updated_columns": mismatched_cols
        })

        if idx < 10:
            print(f"[OK] Updated record {idx + 1}: PK={pk_values}, Columns={mismatched_cols}")

    if len(detailed_mismatch_df) > 10:
        print(f"[INFO] ... and {len(detailed_mismatch_df) - 10} more records")

    print(f"\n[SUMMARY] Total records updated: {update_stats['updated_records']}")
    print(f"[SUMMARY] Total column updates: {update_stats['total_updates']}")
    print(f"[SUMMARY] Columns updated: {dict(update_stats['updated_columns'])}")

    return updated_target_df, update_stats

