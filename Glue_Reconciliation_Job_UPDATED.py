def write_reconciled_target(spark, reconciled_df, output_path, file_format, overwrite_original=False):
    """
    Writes the reconciled target DataFrame to file.
    Respects data types from the original Spark schema (which comes from mapping).
    ENHANCED: Proper NULL handling for all formats (JSON, CSV, Parquet, Excel).
    Supports local output path.
    """
    print_section("WRITING RECONCILED TARGET FILE")

    if overwrite_original:
        print("[INFO] Overwrite mode: Will replace original target file")
    else:
        print("[INFO] Safe mode: Creating new reconciled file")

    print(f"Output path: {output_path}")
    print(f"File format: {file_format}")

    try:
        from pyspark.sql.types import IntegerType, LongType, ShortType, ByteType, DateType, TimestampType

        # Store schema - this comes from your mapping JSON
        original_schema = reconciled_df.schema

        # Identify integer columns from schema (as defined in mapping)
        integer_columns = [
            field.name for field in original_schema.fields
            if isinstance(field.dataType, (IntegerType, LongType, ShortType, ByteType))
        ]

        if integer_columns:
            print(f"[INFO] Integer columns from mapping: {integer_columns}")

        # Prepare dates for JSON/CSV export
        if file_format in ["json", "csv"]:
            print("[INFO] Converting date columns to strings for export...")

            for field in original_schema.fields:
                if isinstance(field.dataType, DateType):
                    reconciled_df = reconciled_df.withColumn(
                        field.name,
                        F.when(
                            F.col(field.name).isNull(),
                            F.lit(None)
                        ).otherwise(
                            F.date_format(F.col(field.name), "M/d/yyyy")
                        )
                    )
                elif isinstance(field.dataType, TimestampType):
                    reconciled_df = reconciled_df.withColumn(
                        field.name,
                        F.when(
                            F.col(field.name).isNull(),
                            F.lit(None)
                        ).otherwise(
                            F.date_format(F.col(field.name), "yyyy-MM-dd HH:mm:ss")
                        )
                    )

        # Convert to pandas
        print("[INFO] Converting Spark DataFrame to Pandas...")
        pandas_df = reconciled_df.toPandas()
        record_count = len(pandas_df)
        print(f"[INFO] Records to write: {record_count}")

        # CRITICAL FIX: Replace NaN with None for proper NULL handling
        print("[INFO] Cleaning NaN values...")
        pandas_df = pandas_df.where(pd.notnull(pandas_df), None)

        # Create output directory
        output_dir = os.path.dirname(output_path)
        if output_dir:  # Only create if there's a directory path
            os.makedirs(output_dir, exist_ok=True)

        # ============================================================
        # JSON FORMAT - NULL as 'null'
        # ============================================================
        if file_format == "json":
            import json

            print("[INFO] Preparing JSON output with proper NULL handling...")

            # Convert integer columns to Int64 (nullable integer) based on mapping
            if integer_columns:
                print("[INFO] Converting integer columns to preserve type in JSON...")
                for col in integer_columns:
                    if col in pandas_df.columns:
                        # Use Int64 which supports NaN -> None conversion
                        pandas_df[col] = pandas_df[col].astype('Int64')
                        # Replace pd.NA with None for JSON serialization
                        pandas_df[col] = pandas_df[col].apply(lambda x: None if pd.isna(x) else x)

            # Convert to records
            records = pandas_df.to_dict(orient='records')

            # Additional cleanup: ensure None instead of NaN in records
            for record in records:
                for key, value in record.items():
                    if pd.isna(value):
                        record[key] = None

            # Write JSON with proper null values
            with open(output_path, 'w') as f:
                json.dump(records, f, indent=2, default=str)

            print(f"[OK] JSON written with NULL as 'null': {output_path}")

        # ============================================================
        # CSV FORMAT - NULL as empty string
        # ============================================================
        elif file_format == "csv":
            print("[INFO] Preparing CSV output with empty strings for NULL...")

            # For CSV, replace None with empty string for cleaner output
            pandas_df_csv = pandas_df.fillna('')
            pandas_df_csv.to_csv(output_path, index=False, na_rep='')

            print(f"[OK] CSV written with NULL as empty string: {output_path}")

        # ============================================================
        # PARQUET FORMAT - Native NULL support
        # ============================================================
        elif file_format == "parquet":
            print("[INFO] Preparing Parquet output with native NULL support...")

            # Parquet has native NULL support, no conversion needed
            # Just ensure proper data types
            try:
                pandas_df.to_parquet(output_path, index=False, engine='pyarrow')
                print(f"[OK] Parquet written with native NULL values: {output_path}")
            except ImportError:
                print("[WARNING] PyArrow not installed, using fastparquet...")
                pandas_df.to_parquet(output_path, index=False, engine='fastparquet')
                print(f"[OK] Parquet written with native NULL values: {output_path}")

        # ============================================================
        # EXCEL FORMAT - NULL as empty cell
        # ============================================================
        elif file_format == "excel":
            print("[INFO] Preparing Excel output with empty cells for NULL...")

            # openpyxl automatically converts None to empty cells
            pandas_df_excel = pandas_df.copy()
            pandas_df_excel.to_excel(output_path, index=False, engine="openpyxl", na_rep='')

            print(f"[OK] Excel written with NULL as empty cells: {output_path}")

        else:
            raise ValueError(f"Unsupported file format: {file_format}")

        # Verify file created
        if os.path.exists(output_path):
            file_size = os.path.getsize(output_path) / (1024 * 1024)
            print(f"[OK] File created: {file_size:.2f} MB")

            # Print NULL handling info
            null_counts = pandas_df.isnull().sum()
            total_nulls = null_counts.sum()
            if total_nulls > 0:
                print(f"[INFO] Total NULL values written: {total_nulls}")
                print(f"[INFO] NULL values by column:")
                for col, count in null_counts[null_counts > 0].items():
                    print(f"       {col}: {count}")
        else:
            raise FileNotFoundError(f"Output file not created: {output_path}")

    except Exception as e:
        print(f"[ERROR] Failed to write: {str(e)}")
        import traceback
        traceback.print_exc()
        raise

